def extract_json_array(raw_text):
    # [ 로 시작해서 ] 로 끝나는 가장 긴 구간을 찾습니다. (점진적 매칭)
    match = re.search(r'\[\s*\{.*\}\s*\]', raw_text, re.DOTALL)
    
    if match:
        json_string = match.group(0) # 매칭된 [ { ... } ] 부분만 추출
        return json_string
    else:
        return "[]"

async def run_vllm(instruction, topis, entity=None) :
    global kanana_client
    response = await kanana_client.chat.completions.create(
        model="kakaocorp/kanana-1.5-8b-instruct-2505",
        messages=[
            {
                "role" : "system",
                "content" : instruction
            },
            {
                "role" : "user",
                "content" : f"교통 공지사항 정보 : {topis}\n entities : {entity}"
            }
        ],
        max_tokens=3000,
        temperature=0.1, # 답변의 일관성을 위해 0.2~0.3 유지 권장
    )
    outputs = response.choices[0].message.content
    try :
        outputs = json.loads(extract_json_array(response.choices[0].message.content))
        print(f"4. vllm outputs : \n{outputs}")
        print("="*30)
        return outputs
    except :
        print(outputs)
        
